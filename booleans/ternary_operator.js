//Ternary Operators
// Ternary operator allow to write a condition. Another way to write conditionals is using ternary operators. Loot at the following examples:

let isRaining = true
isRaining ? console.log("You need a rain coat.") : console.log("No need")

let isRainingOn = false
isRainingOn ? console.log("You need a rain coat.") : console.log("You don't need a rain coat.")


let firstNumber = 5
firstNumber > 0 ? console.log(`${firstNumber} is a positive number.`) : console.log(`${firstNumber} is a negative number.`)

let secondNumber = -5
secondNumber > 0 ? console.log(`${secondNumber} is a positive number.`) : console.log(`${secondNumber} is a negative number.`)


