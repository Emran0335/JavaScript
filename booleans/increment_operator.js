// Increment Operator
// In JavaScript, we use the increment operator to increase a value stored in a variable. The increment could pre or post increment. Let us see each of them:

// 1. Pre-increment

let pre_count = 0
console.log(++pre_count); //++count or count =1 + count or count =+1
console.log(pre_count); //1

// let resultOfPrecount = 1 + pre_count
// console.log(resultOfPrecount);


// 2. Post-increment

let post_count = 0
console.log(post_count++); // result 0, count++ or count = count + 1 or count +=1
console.log(post_count); //1

// We use Post-incremnt most of the time. At least, we should remember how to use post-increment operator.



